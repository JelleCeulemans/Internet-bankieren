<h4>Valse Mail</h4>
<p>Wat zijn de kenmerken van een valse mail als het gaat over internetbankieren:</p>
<ul>
    <li>Er staat in vermeld dat er een veiligheidsprobleem is met je bankkkaart of bankrekening.</li>
    <li>Als er wordt vermeld dat je zeer dringend iets moet doen.</li>
    <li>Al er staat de je het probleem kunt oplossen door op een bijlage te openen of door op een link te klikken. Deze link brengt je dan naar een valse website waar je persoonlijke gegevens moet invullen.</li>
    <li>Phishing mails zijn steeds moeilijker te herkennen. Vaak zijn dit ook perfecte kopieën.</li>
    <li>Er wordt een vals adres van de afzender vermeld.</li>
    <li>Vaak wordt er in valse mails een vervormde link meegegeven. Waar ze zeggen 'klik hier' waar je wordt geleidt naar een valse website. Of zelfs waar ze de volledige link weergeven zoals 'www.voorbeeld.be' en helemaal niet naar die website wordt verwezen. TIP: Bestudeer altijd goed de de URL-link als je wordt doorverwezen.</li>
</ul>

