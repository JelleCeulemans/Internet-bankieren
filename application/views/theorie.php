<h4>Phishing</h4>
<h6>Wat is phishing</h6>
<p>Phishing is een techniek om personen handelingen te laten doen waardoor ze niet in de slachtoffers hun computers moeten inbreken. Cybercriminelen gebruiken alle mogelijke vormen om hun aanval uit te voeren. Zoals via de telefoon, sms, sociale media, e-mail en websites.</p>
<h6>Kenmerken phishing</h6>
<ul>
    <li>Doet zich voor als een betrouwbare partij of zelfs als iemand die je kent.</li>
    <li>E-mails en websites zien er heel geloofwaardig uit.</li>
</ul>
<h6>Wat zijn de bedoelingen van phishing ?</h6>
<p>Hun bedoeling is om jouw vertrouwelijke gegevens zoals inloggegevens, wachtwoorden, bankgegevens,... te bemachtigen of jouw computer te besmetten met malware. Dit gebeurt vaak door bijlage in een mail te openen of een link te volgen naar een website.</p>
<h6>Wat is Malware</h6>
<p>Malware is een verzamelnaam voor allerlei soorten kwaadaardige en schadelijke software die zonder dat je het merkt wordt geïnstalleerd op je computer.</p>
<h6>Wat doet malware ?</h6>
<ul>
    <li>Malware verhindert het normale gebruik op uw computer
    <ul>
        <li>Bij het surfen naar uw bank wordt u ongemerkt doorverwezen naar een andere valse site.</li>
        <li>Tijdens het internetbankieren krijgt u plots een venster te zien waar u bepaalde handelingen moet doen. Zoals bepaalde codes ingeven. Dit komt meestal tevoorschijn als een extra beveiligingscontrole.</li>
    </ul></li>
    <li>Malware steelt informatie
    <ul>
        <li>Spyware scant al uw bestanden op uw computer en volgt uw handelingen en kan zo aan gevoelige informatie komen. Deze informatie wordt gebruikt om door te verkopen, u spam te sturen of betalingen te doen in uw naam.</li>
    </ul></li>
    <li>Malware versleuteld uw bestanden
    <ul>
        <li>Via ransomware ook wel gijzelsoftware (misschien beter gekend als WannaCry van 2017) genaamd worden uw bestanden versleutelt en onbruikbaar gemaakt. Nadien wordt u gecontacteerd om losgeld te betalen. Dit is echter geen garantie dat u uw bestanden gaat terugkrijgen.</li>
    </ul></li>
</ul>

