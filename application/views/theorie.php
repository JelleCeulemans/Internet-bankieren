<h4>Valse Website</h4>
<p>Wat zijn de kenmerken van een valse website als het gaat over internetbankieren:</p>
<ul class="mb-5">
    <li>Let op de notatie voor de URL deze moet beginnen met https (waar de laatste 's' voor 'secure staat) en niet met http. Als je ziet dat de website begint met http zonder 's' en je moet op die website gegevens doorgeven weet je al zeker dat dit geen vertrouwbare website is.</li>
    <li>Steeds meer en meer criminelen gebruiken nu ook al website die beginnen met https. Dus de tip is wees waakzaam.</li>
    <li>Als je toch jouw gegevens doorgeeft op een valse website, kan een cybercrimineel in jouw naam aankopen verrichten of jouw identiteit aannemen om verdere pogingen te doen van oplichting
    </li>
</ul>